char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*save;

	i = 0;
	save = 0;
	if (c == '\0')
	{
		while (s[i] != '\0')
			i++;
		return ((char *)s + i);
	}
	while (s[i] != '\0')
	{
		if (s[i] == c)
			save = (char *)s + i;
		i++;
	}
	return (save);
}
