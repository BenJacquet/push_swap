#include "libft.h"

int		ft_ilen(int n)
{
	int				len;
	unsigned int	nb;

	len = 0;
	if (n < 0)
		len++;
	nb = ft_abs(n);
	while (nb > 9)
	{
		nb = nb / 10;
		len++;
	}
	return (len + 1);
}
